package java_basics;

public class Char_allocation {

	public static void main(String[] args) {
		char ch='a';
		System.out.println(ch);
		
		char ch1=65;
		System.out.println(ch1);
		
		char var='\u00A7';
		System.out.println(var);
		
		char var1='\u20AC';
		System.out.println(var1);
		
		int a = 'A';
		System.out.println(a);

	}

}
